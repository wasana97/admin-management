package com.oop.model;

/**
 * This is the Employee model class
 * 
 * @author Udara Samaratunge, SLIIT
 * @version 1.0
 */
public class Admin {

	
	private String IndexID;
	
	private String CustermorID;
	
	private String CustemorName;
	
	private String DriverName;
	
	private String VehicleNumber;
	
	private String Amount;
	
	
	public String getIndexID() {
		return IndexID;
	}

	public void setIndexID(String indexID) {
		IndexID = indexID;
	}

	public String getCustermorID() {
		return CustermorID;
	}

	public void setCustermorID(String custermorID) {
		CustermorID = custermorID;
	}

	public String getCustemorName() {
		return CustemorName;
	}

	public void setCustemorName(String custemorName) {
		CustemorName = custemorName;
	}

	public String getDriverName() {
		return DriverName;
	}

	public void setDriverName(String driverName) {
		DriverName = driverName;
	}

	public String getVehicleNumber() {
		return VehicleNumber;
	}

	public void setVehicleNumber(String vehicleNumber) {
		VehicleNumber = vehicleNumber;
	}

	public String getAmount() {
		return Amount;
	}

	public void setAmount(String amount) {
		Amount = amount;
	}

	@Override
	public String toString() {
		return "IndexID=" + IndexID + "\n CustermorID=" + CustermorID + "\n CustemorName=" + CustemorName
				+ "\n DriverName=" + DriverName + "\n VehicleNumber=" + VehicleNumber + "\n Amount=" + Amount ;
	}
	
	
	
}
